const u8 TEXT1P[]     = "1 player match";
const u8 TEXT2P[]     = "2 players match";
const u8 TEXTFOOTER[] = "Parisoft 2018";

void cursor_draw(void) {
    if (game.vs_cpu) {
        oam_buffer_spr(50, 21 * 8 - 1, CURSOR_SPR, 0, 1);
    } else {
        oam_buffer_spr(50, 23 * 8 - 1, CURSOR_SPR, 0, 1);
    }
}

void title_draw(void) {
    ppu_set_bg_off();
    ppu_set_spr_off();
    ppu_update();

    ppu_load_spr_pal(PAL);
    ppu_load_bg_pal(PAL);
    ppu_load_attr_table(NT, ATR);
    ppu_load_name_table(NT, (u8*)NAM);
    ppu_load_name_table_section(NT, 21, (SCREEN_WIDTH_TILES - 14) / 2, 1, sizeof(TEXT1P), TEXT1P);
    ppu_load_name_table_section(NT, 23, (SCREEN_WIDTH_TILES - 14) / 2, 1, sizeof(TEXT2P), TEXT2P);
    ppu_load_name_table_section(NT, 27, (SCREEN_WIDTH_TILES - 12) / 2, 1, sizeof(TEXTFOOTER), TEXTFOOTER);

    cursor_draw();

    ppu_wait_vblank();
    ppu_set_bg_on();
    ppu_set_spr_on();
    ppu_update();
}

void title_update(void) {
    if (game.state.start) {
        game.state.start = false;
        game.vs_cpu      = true;
        title_draw();
    } else {
        joy1_poll();

        if ((joy1_pressing(BUTTON_SELECT) && !joy1_pressed(BUTTON_SELECT)) || (joy1_pressing(PAD_DOWN) && !joy1_pressed(PAD_DOWN)) ||
            (joy1_pressing(PAD_UP) && !joy1_pressed(PAD_UP))) {
            game.vs_cpu = !game.vs_cpu;
            cursor_draw();
        } else if (joy1_pressing(BUTTON_START) || joy1_pressing(BUTTON_A)) {
            state_next();
        }
    }
}